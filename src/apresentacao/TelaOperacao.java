    private void btnExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecutarActionPerformed
        try {
            ContaCorrente cc = new NContaCorrente().consultarPorNumero(jTextFieldNumConta.getText());
            String numeroCC = cc.getConta().getNumero();
            double saldo = cc.getConta().getSaldo();
            double valor = Double.parseDouble(jTextFieldValor.getText());
            String senha = jTextFieldSenha.getText();

            if (buttonGroupOperacoes.getSelection() == null) {
                throw new Exception("Selecione uma operação!");
            }

            if (jRadioButtonSaque.isSelected()) {
                Saque saque = new Saque();
                saque.executarOperacao(cc. getLimite(), saldo, valor, "saque", numeroCC, idTitularOperacao, senha);
                jTextAreaExtrato.setText(jTextAreaExtrato.getText() + "\nSacando R$ " + df.format(valor) + "...");
            } else if (jRadioButtonDeposito.isSelected()) {
                Deposito deposito = new Deposito();
                deposito.executarOperacao(cc.getLimite(), saldo, valor, "deposito", numeroCC, idTitularOperacao, senha);
                jTextAreaExtrato.setText(jTextAreaExtrato.getText() + "\nDepositando R$ " + df.format(valor) + "...");
            } else if (jRadioButtonExtrato.isSelected()) {
                String resultado = "";

                for (Movimentacao mov : new NMovimentacao().listar()) {
                    if (mov.getId_conta() == cc.getConta().getId()) {
                        resultado += "Data : " + fmt.format(mov.getData()) + "\n"
                                + "Tipo : " + mov.getTipo() + "\n"
                                + "Valor: R$ " + df.format(mov.getValor()) + "\n"
                                + " ----------------------- \n";
                    }
                }
                jTextAreaExtrato.setText(resultado + "Saldo atual: R$ " + df.format(cc.getConta().getSaldo())
                        + "\nLimite: R$ " + df.format(cc.getLimite())
                        + "\nSaldo com limite: R$ " + df.format(cc.getLimite() + cc.getConta().getSaldo()));
            }
            System.out.println();
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção!", 2);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Atenção!", 2);
        }
    }//GEN-LAST:event_btnExecutarActionPerformed

    private void jButtonBuscaTitularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscaTitularActionPerformed
        try {
            if (jComboBoxAgencia.getSelectedIndex() == 0) {
                throw new Exception("Selecione uma Agência!");
            }
            if (jTextFieldNumConta.getText().isEmpty()) {
                throw new Exception("Informe um número de conta!");
            }
            carregarTabelaTitular();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção!", 2);
        }
    }//GEN-LAST:event_jButtonBuscaTitularActionPerformed

    private void jTableTitularesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTitularesMousePressed
        try {
            int linha = jTableTitulares.getSelectedRow();
            idTitularOperacao = Integer.parseInt(jTableTitulares.getValueAt(linha, 0).toString());
            Pessoa pessoa = new NPessoa().consultar(idTitularOperacao);
            jTextFieldTitularSelecionado.setText(pessoa.getNome());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTableTitularesMousePressed

