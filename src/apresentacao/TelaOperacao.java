    private void carregarComboAgencia() {
        try {
            for (Agencia agencia : new NAgencia().listar()) {
                jComboBoxAgencia.addItem(agencia.getNumero());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro!", 2);;
        }
    }

    private void carregarTabelaTitular() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTableTitulares.getModel();
            PPessoaConta ppc = new PPessoaConta();
            ContaCorrente cc = new PContaCorrente().consultarPorNumero(jTextFieldNumConta.getText());

            for (PessoaConta p : ppc.listar()) {
                if (p.getId_conta() == cc.getConta().getId()) {
                    Pessoa pessoa = new NPessoa().consultar(p.getId_pessoa());
                    String[] linha = new String[]{pessoa.getId() + "", pessoa.getNome()};
                    modelo.addRow(linha);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro!", 2);
        }
    }

