    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
            Pessoa pessoa = new Pessoa();
            NPessoa nPessoa = new NPessoa();

            if (jTextFieldNome.getText().isEmpty()) {
                throw new Exception("E necessário preencher o nome.");
            }
            if (jTextFieldCPF.getText().isEmpty()) {
                throw new Exception("E necessário preencher o CPF.");
            }
            if (jTextFieldTelefone.getText().isEmpty()) {
                throw new Exception("E necessário preencher o telefone.");
            }
            if (jTextFieldEmail.getText().isEmpty()) {
                throw new Exception("E necessário preencher o e-mail.");
            }
            if (jTextFieldSenha.getText().isEmpty()) {
                throw new Exception("E necessário digitar a senha.");
            }
            if (jTextFieldCEP.getText().isEmpty()) {
                throw new Exception("Informe o CEP.");
            }
            if (jTextFieldLogradouro.getText().isEmpty()) {
                throw new Exception("Informe o nome da rua1");
            }
            if (jTextFieldBairro.getText().isEmpty()) {
                throw new Exception("Está faltando o bairro.");
            }
            if (jTextFieldCidade.getText().isEmpty()) {
                throw new Exception("Está faltando a cidade.");
            }
            if (jTextFieldUF.getText().isEmpty()) {
                throw new Exception("Está faltando a unidade federativa.");
            }
            
            pessoa.setNome(jTextFieldNome.getText());
            pessoa.setCpf(jTextFieldCPF.getText());
            pessoa.setTelefone(jTextFieldTelefone.getText());
            pessoa.setEmail(jTextFieldEmail.getText());
            pessoa.setSenha(jTextFieldSenha.getText());
            pessoa.setLogradouro(jTextFieldLogradouro.getText());
            pessoa.setBairro(jTextFieldBairro.getText());
            pessoa.setCidade(jTextFieldCidade.getText());
            pessoa.setUf(jTextFieldUF.getText());
            pessoa.setCep(jTextFieldCEP.getText());
            if (!jTextFieldPessoaID.getText().isEmpty()) {

                pessoa.setId(Integer.parseInt(jTextFieldPessoaID.getText()));
            }
            if (jRadioButtonCliente.isSelected()) {
                pessoa.setTipo_pessoa(1);
                nPessoa.salvar(pessoa);
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
                limpar();
            } else if (jRadioButtonFuncionario.isSelected()) {
                pessoa.setTipo_pessoa(2);
                nPessoa.salvar(pessoa);
                JOptionPane.showMessageDialog(null, "Funcionário cadastrado com sucesso!");
                limpar();
            } else {
                JOptionPane.showMessageDialog(null, "Escolha cliente ou funcionário!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro!", 2);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

