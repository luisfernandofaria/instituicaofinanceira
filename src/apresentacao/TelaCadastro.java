    private void carregarTabela() {

        try {
            PPessoa pPessoa = new PPessoa();
            Iterator pessoas = pPessoa.listaPessoas();

            while (pessoas.hasNext()) {
                Pessoa pessoa = (Pessoa) pessoas.next();
                if (pessoa.getTipo_pessoa() == 1) {
                    DefaultTableModel modelo = (DefaultTableModel) jTableClientes.getModel();
                    String[] linha = new String[]{pessoa.getId() + "", pessoa.getNome() + "",
                        pessoa.getCpf()};
                    modelo.addRow(linha);
                    jTableClientes.setAutoCreateRowSorter(true);
                } else {
                    DefaultTableModel modelo = (DefaultTableModel) jTableFuncionarios.getModel();
                    String[] linha = new String[]{pessoa.getId() + "", pessoa.getNome() + "",
                        pessoa.getCpf()};
                    modelo.addRow(linha);
                    jTableFuncionarios.setAutoCreateRowSorter(true);
                }
            }
        } catch (Exception e) {
            e.getMessage();
            e.printStackTrace();
        }
    }

