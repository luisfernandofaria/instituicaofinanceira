    private void jTableClientesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMousePressed
        try {
            limpar();
            int linha = jTableClientes.getSelectedRow();
            id = Integer.parseInt(jTableClientes.getValueAt(linha, 0).toString());

            Pessoa pessoa = new NPessoa().consultar(id);
            ContaCorrente cc = new NContaCorrente().consultar(new PPessoaConta().consultarPorIDPessoa(id).getId_conta());

            jTextFieldNomeClientes.setText(pessoa.getNome());
            jTextFieldEmailCliente.setText(pessoa.getEmail());
            jTextFieldEnderecoCliente.setText(pessoa.getLogradouro() + ", " + pessoa.getBairro()
                    + ", " + pessoa.getCidade() + ", " + pessoa.getUf() + " - " + pessoa.getCep());
            jTextFieldTelefoneCliente.setText(pessoa.getTelefone());
            jTextFieldContaCorrente.setText(cc.getConta().getNumero());
            jTextFieldLimiteCC.setText(Double.toString(cc.getLimite()));
            jTextFieldDataAberturC.setText(cc.getConta().getData_abertura().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cliente não possui nenhuma conta!");
        }
    }//GEN-LAST:event_jTableClientesMousePressed

