    private void buscarDados() {
        try {
            jLabelCliente.setText(pessoa.getNome());
            for (ContaCorrente cc : new NContaCorrente().listar()) {
                pPessoaConta = new PPessoaConta();
                if (pPessoaConta.consultar(cc.getConta().getId()).getId_pessoa() == pessoa.getId()) {
                    jComboBoxContaSaq.addItem(cc.getConta().getNumero());
                    contaAtual = cc;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

