    private void jButtonExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarActionPerformed
        try {
            Double valorOP = Double.parseDouble(jFormattedTextFieldValor.getText());
            ContaCorrente contaDeposito = new NContaCorrente().consultarPorNumero(jTextFieldConta.getText());
            if (jComboBoxContaSaq.getSelectedIndex() == 0) {
                throw new Exception("Selecione a conta!");
            }
            switch (op) { 
                case 1:
                    if (jPasswordFieldSenha.getText().isEmpty()) {
                        throw new Exception("Informe a senha!");
                    }
                    if (jFormattedTextFieldValor.getText().isEmpty()) {
                        throw new Exception("Informe o valor da operaÃ§Ã£o!");
                    }
                    Saque saque = new Saque();
                    //como passar essa validaÃ§Ã£o para a camada de negÃ³cio?
                    if ((contaAtual.getConta().getSaldo()+contaAtual.getLimite()) - valorOP < 0) {
                        throw new Exception("Saldo insuficiente!");
                    } else {
                        saque.executarOperacao(contaAtual.getLimite(), contaAtual.getConta().getSaldo(),
                                valorOP, "saque", contaAtual.getConta().getNumero(),
                                pessoa.getId(), "senha...");
                        jTextAreaResultado.setText("Sacando.......................R$ " + df.format(valorOP) + "\n"
                                + "Saldo atual:..................R$ " + (contaAtual.getConta().getSaldo() - valorOP));
                        //necessÃ¡rio subtrair pois sÃ³ atualiza o saldo depois que termina a operaÃ§Ã£o
                    }
                    break;
                case 2:
                    if (jTextFieldAgencia.getText().isEmpty()) {
                        throw new Exception("Preencha o nÃºmero da AgÃªncia!");
                    }
                    if (jTextFieldConta.getText().isEmpty()) {
                        throw new Exception("Preencha o nÃºmero da Conta!");
                    }
                    if (jFormattedTextFieldValor.getText().isEmpty()) {
                        throw new Exception("Informe o valor da operaÃ§Ã£o!");
                    }
                    Deposito deposito = new Deposito();
                    deposito.executarOperacao(contaDeposito.getLimite(), contaDeposito.getConta().getSaldo(),
                            valorOP, "saque", contaDeposito.getConta().getNumero(),
                            pessoa.getId(), "senha...");
                    jTextAreaResultado.setText("Depositando...................R$ " + df.format(valorOP) + "\n");
                    break;
                default:
                    break;
            }
            jButtonExecutar.setEnabled(false);
            setarInvisivel();
        } catch (RuntimeException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "AtenÃ§Ã£o!", 2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "AtenÃ§Ã£o!", 2);
        }
    }//GEN-LAST:event_jButtonExecutarActionPerformed

    private void jButtonExtratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExtratoActionPerformed
        try {
            String resultado = "";

            for (Movimentacao mov : new NMovimentacao().listar()) {
//                pPessoaConta = new PPessoaConta();
                if (mov.getId_conta() == contaAtual.getConta().getId()) {
                    resultado += "Data : " + mov.getData() + "\n"
                            + "Tipo : " + mov.getTipo() + "\n"
                            + "Valor: R$ " + df.format(mov.getValor()) + "\n"
                            + " ----------------------- \n";
                }
            }
            jTextAreaResultado.setText(resultado + "Saldo atual: R$ " + df.format(contaAtual.getConta().getSaldo())
                    + "\nLimite: R$ " + df.format(contaAtual.getLimite())
                    + "\nSaldo com limite: R$ " + df.format(contaAtual.getLimite() + contaAtual.getConta().getSaldo()));
            jButtonExecutar.setEnabled(false);
            setarInvisivel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Conta sem movimentaÃ§Ã£o!", "AtenÃ§Ã£o!", 2);
        }
    }//GEN-LAST:event_jButtonExtratoActionPerformed

